public class Arvores {
  // Árvore é representada por nós conectados por arestas;
  // Árvore binária tem 2 filhos

  // Caminho - Caminho refere-se à sequência de nós ao longo das bordas de uma
  // árvore;
  // Raiz - O nó na parte superior da árvore é chamado raiz. Existe apenas uma
  // raizpor árvore e um caminho do nó raiz para qualquer nó;
  // Pai - Qualquer nó, exceto o nó raiz, tem uma borda para cima em relação a
  // umnó chamado pai;
  // Filho - O nó abaixo de um determinado nó conectado por sua borda descendente
  // é chamado de nó filho;
  // Folha - O nó que não possui nenhum nó filho é chamado de nó folha;
  // Subárvore - Subárvore representa os descendentes de um nó;
  // Visitar - Visitar refere-se à verificação do valor de um nó quando o controle
  // está no nó;
  // Atravessar - Atravessar significa passar pelos nós em uma ordem específica;
  // Níveis - o nível de um nó representa a geração de um nó. O nó raiz está no
  // nível 0, o próximo nó filho está no nível 1, o neto está no nível 2 e assim
  // por diante;
  // Grau - O número de sub-árvores de um nó é o grau do nó. O grau da árvore é o
  // maior valor de grau de nó entre todos os nós da árvore;
  // Chave - Chave representa um valor de um nó com base no qual uma operação de
  // pesquisa deve ser realizada;


}
